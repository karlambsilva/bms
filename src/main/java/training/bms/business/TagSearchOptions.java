package training.bms.business;

public class TagSearchOptions {
	
	private String id;
	private String name;
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getId() {
		return id;
	}
	
	public void setId(String id) {
		this.id = id;
	}
	
	@Override
	public String toString() {
		return "TagSearchOptions [name=" + name + "]";
	}

}
